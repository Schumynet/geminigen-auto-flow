const AutoFlow = {
  isRunning: false,
  isPaused: false,
  currentClipIndex: 0,
  clips: [],
  config: {
    clipDuration: 6,
    autoCombine: true,
    delayBetween: 2000,
    contextMode: 'character' // 'character' | 'environment' | 'both'
  },
  
  start(clips, config = {}) {
    this.clips = clips;
    this.config = { ...this.config, ...config };
    this.isRunning = true;
    this.isPaused = false;
    this.currentClipIndex = 0;
    this.broadcast();
  },
  
  pause() {
    this.isPaused = true;
    this.broadcast();
  },
  
  resume() {
    this.isPaused = false;
    this.broadcast();
  },
  
  stop() {
    this.isRunning = false;
    this.isPaused = false;
    this.broadcast();
  },
  
  nextClip() {
    this.currentClipIndex++;
    if (this.currentClipIndex >= this.clips.length) {
      this.stop();
    }
    this.broadcast();
  },
  
  getCurrentClip() {
    return this.clips[this.currentClipIndex];
  },
  
  getProgress() {
    return {
      current: this.currentClipIndex + 1,
      total: this.clips.length,
      percentage: Math.round(((this.currentClipIndex + 1) / this.clips.length) * 100),
      clip: this.getCurrentClip()
    };
  },
  
  broadcast() {
    chrome.runtime.sendMessage({ 
      type: 'FLOW_UPDATE', 
      flow: { isRunning: this.isRunning, isPaused: this.isPaused },
      progress: this.getProgress()
    }).catch(() => {});
  }
};

