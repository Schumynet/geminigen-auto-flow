// SCRIPT PARSER - Suddivide copione in scene/prompt
// ═══════════════════════════════════════════════════════════

const ScriptParser = {
  // Pattern da ESCLUDERE (rumore)
  noisePatterns: [
    // Header/Footer comuni
    /^\s*✅.*PROMPT/i,
    /^\s*✅\s*TUTTI\s*I\s*\d+\s*PROMPT\s*COMPLETATI/i,
    /^\s*✅\s*CONTINUAZIONE/i,
    /^\s*✅\s*ECC.*/i,
    /^\s*\[DNA.*\]/i,
    /^\s*DNA\s+APPLICATO/i,
    /^\s*Vuoi\s+che\s+ti\s+.*/i,
    /^\s*Ogni\s+\d+\s+prompt/i,
    /^\s*---\s*$/,
    
    // DNA ripetuto
    /Ridley Scott cinematic documentary/i,
    /heavy film grain/i,
    /anamorphic lens/i,
    /warm sunset color grading/i,
    /volumetric god rays/i,
    /dust particles/i,
    /photorealistic 4K/i,
    
    // Strutture fisse
    /Crea un video cinematico di esattamente \d+ secondi/i,
    /^\s*Descrizione visiva:\s*$/i,
    /^\s*Voiceover:\s*$/i,
    /^\s*Suono di sottofondo:\s*$/i,
    /^\s*DNA\s*:\s*$/i,
    
    // Batch headers
    /BATCH\s*\d+\s*\(\s*\d+-\d+\s*\)/i,
    /^\s*BATCH\s*\d+/i,
    
    // Numerazione prompt
    /PROMPT\s+\d+\s*\n/i,
    
    // Footer
    /Prodotto da Space Archive/i,
    /Iscriviti per altri/i,
    /Grazie per aver guardato/i,
  ],
  
  // Pattern che indicano nuova scena/prompt
  promptPatterns: [
    /^PROMPT\s+(\d+)/i,
    /^\[PROMPT\s+(\d+)\]/i,
    /^\d+\s*[\.\)]\s*(.+)/,
    /^Clip\s+(\d+)/i,
    /^SCENA\s+(\d+)/i,
  ],
  
  // Estrai solo la descrizione visiva (o contenuto significativo)
  extractVisualDescription(text) {
    // Cerca "Descrizione visiva:" e prendi tutto dopo
    const descMatch = text.match(/Descrizione visiva:\s*(.+?)(?=\s*(?:Voiceover|Suono|$))/is);
    if (descMatch) {
      return descMatch[1].trim();
    }
    
    // Altrimenti prendi tutto dopo il DNA (se presente)
    const dnaMatch = text.match(/DNA[\s:]*[^\n]*\n(.+)/is);
    if (dnaMatch) {
      let cleaned = dnaMatch[1];
      // Rimuovi voiceover e suono
      cleaned = cleaned.replace(/Voiceover:.*?(?=\n|$)/gis, '');
      cleaned = cleaned.replace(/Suono di sottofondo:.*?(?=\n|$)/gis, '');
      return cleaned.trim();
    }
    
    // Altrimenti prendi tutto
    return text;
  },
  
  // Pulisci una linea dal rumore
  cleanLine(line) {
    let cleaned = line.trim();
    
    // Rimuovi prefissi come "PROMPT 001"
    cleaned = cleaned.replace(/^PROMPT\s+\d+\s*/i, '');
    cleaned = cleaned.replace(/^\d+\s*[\.\)]\s*/, '');
    cleaned = cleaned.replace(/^-\s*/, '');
    
    // Rimuovi etichette come "DNA:", "Descrizione visiva:", ecc.
    cleaned = cleaned.replace(/^(DNA|Descrizione visiva|Voiceover|Suono di sottofondo)\s*:\s*/gi, '');
    
    return cleaned;
  },
  
  // Verifica se è una linea di rumore
  isNoise(line) {
    const trimmed = line.trim();
    
    // Linee vuote non sono rumore
    if (!trimmed) return false;
    
    // Check contro tutti i pattern di rumore
    for (const pattern of this.noisePatterns) {
      if (pattern.test(trimmed)) return true;
    }
    
    // Linea troppo corta
    if (trimmed.length < 5) return true;
    
    // Solo numeri/punteggiatura
    if (/^[0-9\s\.\,\-\(\)]+$/.test(trimmed)) return true;
    
    return false;
  },
  
  // Parse copione completo - estrae VISIVA + VOCE + SUONO
  parseScript(text) {
    const scenes = [];
    
    // Pulisci il testo base
    let cleanText = text;
    
    // Rimuovi header comuni all'inizio
    cleanText = cleanText.replace(/^[\s\S]*?(?=PROMPT\s+\d+|^\d+\s*[\.\)]|BATCH\s*\d)/i, '');
    
    // Rimuovi footer comuni alla fine  
    cleanText = cleanText.replace(/(Prodotto da|Vuoi che|Raggruppa|Iscriviti|z-ai)[\s\S]*$/i, '');
    
    // Suddividi per PROMPT (ogni PROMPT = una scena)
    const promptMatches = cleanText.matchAll(/PROMPT\s+(\d+)(?:\s*\n|\s*)([\s\S]*?)(?=(?:\s*PROMPT\s+\d+)|$)/gi);
    
    for (const match of promptMatches) {
      const promptNumStr = match[1];
      const promptContent = match[2];
      
      // Estrai tutti i campi
      const fields = this.extractAllFields(promptContent);
      
      // Usa il NUMERO REALE dal testo (001, 002, ecc.)
      const realPromptNum = parseInt(promptNumStr, 10);
      
      if (fields.visualDescription.length > 10 || fields.voiceover.length > 5) {
        scenes.push({
          index: scenes.length,  // Indice sequenziale
          title: `Prompt ${realPromptNum}`,  // Numero reale nel titolo
          visualDescription: fields.visualDescription,
          voiceover: fields.voiceover,
          audio: fields.audio,
          duration: 6,
          promptNumber: realPromptNum  // Numero reale
        });
      }
    }
    
    // Se non abbiamo trovato PROMPT, prova metodo alternativo
    if (scenes.length === 0) {
      return this.parseScriptAlternative(cleanText);
    }
    
    console.log(`[ScriptParser] Parsed ${scenes.length} prompts with full fields`);
    return scenes;
  },
  
  // Estrai tutti i campi da un prompt
  extractAllFields(content) {
    const result = {
      visualDescription: '',
      voiceover: '',
      audio: ''
    };
    
    // Estrai Descrizione visiva
    const visualMatch = content.match(/Descrizione visiva:\s*(.+?)(?=\s*(?:Voiceover|Suono|$))/is);
    if (visualMatch) {
      result.visualDescription = visualMatch[1].trim();
    }
    
    // Estrai Voiceover
    const voiceMatch = content.match(/Voiceover:\s*(.+?)(?=\s*(?:Suono|$))/is);
    if (voiceMatch) {
      result.voiceover = voiceMatch[1].trim();
    }
    
    // Estrai Suono di sottofondo
    const audioMatch = content.match(/Suono di sottofondo:\s*(.+?)(?=\s*(?:$))/is);
    if (audioMatch) {
      result.audio = audioMatch[1].trim();
    }
    
    // Se non abbiamo estratto niente, prova a pulire tutto
    if (!result.visualDescription && !result.voiceover) {
      // Rimuovi DNA e prendi il resto
      let cleaned = content
        .replace(/DNA[\s:]*[^\n]*\n/gi, '')
        .replace(/Crea un video cinematico.*?\n/gi, '');
      
      // Separa per linea
      const lines = cleaned.split('\n').filter(l => l.trim());
      
      for (const line of lines) {
        const trimmed = line.trim();
        
        if (trimmed.toLowerCase().startsWith('voiceover:')) {
          result.voiceover = trimmed.replace(/^voiceover:\s*/i, '').trim();
        } else if (trimmed.toLowerCase().startsWith('suono')) {
          result.audio = trimmed.replace(/^suono[\s\w]*:\s*/i, '').trim();
        } else if (trimmed.length > 10 && !this.isNoise(trimmed)) {
          result.visualDescription = trimmed;
        }
      }
    }
    
    return result;
  },
  
  // Metodo alternativo: ogni riga non vuota = prompt
  parseScriptAlternative(text) {
    const scenes = [];
    const lines = text.split('\n');
    
    let currentScene = null;
    let promptNum = 1;
    
    for (const line of lines) {
      const trimmed = line.trim();
      
      // Salta se è rumore
      if (this.isNoise(trimmed)) continue;
      
      // Pulisci la linea
      const cleaned = this.cleanLine(trimmed);
      if (cleaned.length < 10) continue;
      
      // Nuovo prompt se c'è separatore
      if (/^[-_]{3,}$/.test(trimmed) || /^(BATCH|PROMPT|Clip|SCENA)\s*\d+/i.test(trimmed)) {
        // Salva quello precedente
        if (currentScene && currentScene.visualDescription) {
          scenes.push(currentScene);
          promptNum++;
        }
        currentScene = {
          index: scenes.length,
          title: `Prompt ${promptNum}`,
          visualDescription: '',
          voiceover: '',
          audio: '',
          duration: 6,
          promptNumber: promptNum,
          character: null,
          environment: null
        };
        continue;
      }
      
      // Classifica la linea
      if (!currentScene) {
        currentScene = {
          index: scenes.length,
          title: `Prompt ${promptNum}`,
          visualDescription: '',
          voiceover: '',
          audio: '',
          duration: 6,
          promptNumber: promptNum,
          character: null,
          environment: null
        };
      }
      
      if (trimmed.toLowerCase().startsWith('voiceover:')) {
        currentScene.voiceover = trimmed.replace(/^voiceover:\s*/i, '').trim();
      } else if (trimmed.toLowerCase().startsWith('suono')) {
        currentScene.audio = trimmed.replace(/^suono[\s\w]*:\s*/i, '').trim();
      } else {
        currentScene.visualDescription = cleaned;
      }
    }
    
    // Salva l'ultimo
    if (currentScene && currentScene.visualDescription) {
      scenes.push(currentScene);
    }
    
    console.log(`[ScriptParser] Alt method: ${scenes.length} prompts with full fields`);
    return scenes;
  },
  
  // Combina scene per clip lunga
  combineForLongClip(scenes, targetDuration) {
    const combined = [];
    let currentGroup = [];
    let currentDuration = 0;
    const clipsPerGroup = Math.ceil(targetDuration / 6);
    
    for (const scene of scenes) {
      currentGroup.push(scene);
      
      if (currentGroup.length >= clipsPerGroup) {
        combined.push({
          scenes: [...currentGroup],
          prompt: this.buildCombinedPrompt(currentGroup),
          duration: Math.min(targetDuration, currentGroup.length * 6)
        });
        currentGroup = [];
      }
    }
    
    // Aggiungi gruppi rimanenti
    if (currentGroup.length > 0) {
      combined.push({
        scenes: [...currentGroup],
        prompt: this.buildCombinedPrompt(currentGroup),
        duration: currentGroup.length * 6
      });
    }
    
    return combined;
  },
  
  // Costruisce prompt combinato da scene
  buildCombinedPrompt(scenes) {
    return scenes.map(s => {
      const titlePart = s.title ? `[${s.title}] ` : '';
      return titlePart + s.prompts.join(' ');
    }).join(' | ');
  }
};

