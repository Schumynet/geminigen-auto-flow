// AUTO GENERATOR - Genera personaggi/ambienti se mancanti
// ═══════════════════════════════════════════════════════════

const AutoGenerator = {
  // Genera personaggio da copione/scene
  generateCharacterFromScript(scenes) {
    // Analizza il copione per identificare personaggi
    const allText = scenes.map(s => s.title + ' ' + s.prompts.join(' ')).join(' ');
    
    // Pattern comuni per identificare personaggi
    const patterns = [
      /(?:un|uomo|una|donna|bambino|bambina|ragazzo|ragazza|personaggio|protagonista|antagonista|eroe|villano)\s*(?:di\s+)?([^,\.]+)/i,
      /([A-Z][a-z]+)\s+(?:entra|esce|appare|scompare|cammina|corre|grida|parla)/,
      /([A-Z][a-z]+):\s*(.+)/,
      /(?:il|l'|la)\s+([a-z]+)\s+(?:è|ha|sembra)/i
    ];
    
    let characterName = null;
    let characterDesc = null;
    
    for (const pattern of patterns) {
      const match = allText.match(pattern);
      if (match) {
        characterName = match[1] || match[2] || 'Protagonista';
        characterDesc = this.extractCharacterDescription(allText, characterName);
        break;
      }
    }
    
    if (!characterName) {
      // Genera personaggio generico basato sul tono del copione
      const tone = this.detectTone(scenes);
      characterName = tone.defaultCharacter;
      characterDesc = tone.defaultDesc;
    }
    
    return {
      id: 'auto_char_' + Date.now(),
      name: characterName,
      description: characterDesc,
      prompt: this.buildCharacterPrompt(characterName, characterDesc),
      autoGenerated: true,
      createdAt: Date.now()
    };
  },
  
  // Genera ambiente da copione/scene
  generateEnvironmentFromScript(scenes) {
    const allText = scenes.map(s => s.title + ' ' + s.prompts.join(' ')).join(' ');
    
    // Pattern per ambienti
    const patterns = [
      /(?:in|nel|alla|nella)\s+([^,\.]+)/i,
      /(?:dentro|dentro\s+a)\s+([^,\.]+)/i,
      /(?:luogo|posto|location|scenario):\s*([^,\.]+)/i,
      /(?:accade|avviene|svolge)\s+(?:in|al)\s+([^,\.]+)/i
    ];
    
    let envName = null;
    let envDesc = null;
    let envMood = 'neutral';
    let envLighting = 'natural';
    
    for (const pattern of patterns) {
      const match = allText.match(pattern);
      if (match && match[1].length > 2) {
        envName = match[1].trim();
        envDesc = this.extractEnvironmentDescription(allText, envName);
        break;
      }
    }
    
    if (!envName) {
      const tone = this.detectTone(scenes);
      envName = tone.defaultEnvironment;
      envDesc = tone.defaultEnvDesc;
      envMood = tone.mood;
      envLighting = tone.lighting;
    }
    
    return {
      id: 'auto_env_' + Date.now(),
      name: envName,
      description: envDesc,
      mood: envMood,
      lighting: envLighting,
      autoGenerated: true,
      createdAt: Date.now()
    };
  },
  
  // Estrai descrizione personaggio
  extractCharacterDescription(text, name) {
    const sentences = text.split(/[.!]/);
    for (const sentence of sentences) {
      if (sentence.toLowerCase().includes(name.toLowerCase())) {
        // Estrai descrizione dalla frase
        const desc = sentence.trim();
        if (desc.length > 10 && desc.length < 200) {
          return desc;
        }
      }
    }
    return this.generateGenericDescription(text);
  },
  
  // Genera descrizione generica
  generateGenericDescription(text) {
    const hasDark = /dark|notte|oscur|tenebr/i.test(text);
    const hasAction = /esplos|azion|cors|combatt/i.test(text);
    const hasSciFi = /spaz|alien|robot|tecnolog/i.test(text);
    
    if (hasSciFi) return 'A mysterious figure with futuristic attire';
    if (hasAction) return 'An athletic person in dynamic pose';
    if (hasDark) return 'A shadowy silhouette in dark atmosphere';
    return 'A person with cinematic presence';
  },
  
  // Estrai descrizione ambiente
  extractEnvironmentDescription(text, name) {
    const sentences = text.split(/[.!]/);
    for (const sentence of sentences) {
      if (sentence.toLowerCase().includes(name.toLowerCase())) {
        const desc = sentence.trim();
        if (desc.length > 10 && desc.length < 200) {
          return desc;
        }
      }
    }
    return `Location: ${name}. Cinematic setting.`;
  },
  
  // Costruisci prompt personaggio
  buildCharacterPrompt(name, description) {
    return `${name}. ${description}. Cinematic style, detailed, high quality rendering.`;
  },
  
  // Rileva tono del copione
  detectTone(scenes) {
    const allText = scenes.map(s => s.prompts.join(' ')).join(' ').toLowerCase();
    
    if (/alien|spaz|galax|rocket|astro/i.test(allText)) {
      return {
        mood: 'mysterious',
        lighting: 'dramatic neon',
        defaultCharacter: 'Astronaut',
        defaultDesc: 'A space explorer in futuristic suit',
        defaultEnvironment: 'Space Station',
        defaultEnvDesc: 'Futuristic space station with neon lights and control panels'
      };
    }
    
    if (/uffici|azienda|reunion|computer/i.test(allText)) {
      return {
        mood: 'professional',
        lighting: 'office fluorescent',
        defaultCharacter: 'Business Person',
        defaultDesc: 'A professional in smart casual attire',
        defaultEnvironment: 'Office',
        defaultEnvDesc: 'Modern office with desks and computers'
      };
    }
    
    if (/foresta|alber|natura|selvag/i.test(allText)) {
      return {
        mood: 'serene',
        lighting: 'natural sunlight',
        defaultCharacter: 'Wanderer',
        defaultDesc: 'A traveler in outdoor clothing',
        defaultEnvironment: 'Forest',
        defaultEnvDesc: 'Dense forest with sunlight filtering through trees'
      };
    }
    
    // Default cinematic
    return {
      mood: 'dramatic',
      lighting: 'cinematic',
      defaultCharacter: 'Mysterious Figure',
      defaultDesc: 'A silhouetted person in dramatic lighting',
      defaultEnvironment: 'Cinematic Scene',
      defaultEnvDesc: 'Dramatic setting with cinematic lighting and atmosphere'
    };
  }
};

